char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	if (c == 0)
	{
		while (s[i])
			i++;
		return ((char *)(s + i));
	}
	while (*s)
	{
		if (*s == c % 256)
			return ((char *)s);
		s++;
	}
	return (0);
}
