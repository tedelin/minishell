#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;

	i = ft_strlen(s);
	while (i + 1 > 0)
	{
		if (s[i] == c % 256)
			return ((char *)(s + i));
		i--;
	}
	return (0);
}
